TEST(testbuildprogram, simple) {
    EasyCL *cl = EasyCL::createForFirstGpuOtherwiseCpu();
    try {
        std::vector<CLKernel *> kernels =  cl->buildProgramFromString(getKernel1(),"","testbuildprogram1.cl");
        EXPECT_TRUE(kernels.size() == 3);

        cout << kernels[0]->getKernelName();
        //cl->buildKernelFromString(getKernel(), "foo", "", "testbuildlog.cl");
        std::map<std::string,int> m;
        for(int i=0;i<3;++i){
           m[kernels[i]->getKernelName()] = 1;
        }

       EXPECT_TRUE(m.size() == 3);

    } catch(std::runtime_error err) {
        cout << err.what() << endl;
        EXPECT_TRUE(string(err.what()).find("testbuildprogram1.cl") != string::npos);
    }
    delete cl;
}

